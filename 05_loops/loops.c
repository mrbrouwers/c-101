/*
    LES 5: Loops (while / for)

    Loops kunnen we gebruiken om stukken code meerdere keren uit te voeren, maar
    niet op dezelfde manier als bij functies. Bij functies roepen we ze één
    of meerdere keren aan, maar bij loops herhalen we een stuk code totdat aan een bepaalde
    voorwaarde is voldaan.

    Er zijn twee veelgebruikte soorten loops in C: de "while" loop en de "for" loop.

    1. while-loop:
    De while-loop blijft een stuk code uitvoeren zolang een bepaalde voorwaarde waar is.

    Voorbeeld:
    int i = 0;
    while (i < 5) {
        printf("%d\n", i);
        i++; // verhoog i met 1
    }

    2. for-loop:
    De for-loop is handig als je weet hoe vaak je iets wilt herhalen.
    Als je bijvoorbeeld iets 5 keer wilt doen, gebruik je een for-loop.

    Voorbeeld:
    for (int i = 0; i < 5; i++) {
        printf("%d\n", i);
    }
*/

